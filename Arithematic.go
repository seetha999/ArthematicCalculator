package main
import "fmt"
func addNumber(a, b int) int {
	return a + b
}
func sub(a, b int) int {
	return a - b
}
func mul(a, b int) int {
	return a * b
}
func division(a, b int) int {
	return a / b
}
func main() {
	var a, b, c, d, e, f int
	a = 18
	b = 9
	c = addNumber(a, b)
	d = sub(a, b)
	e = mul(a, b)
	f = division(a, b)
	fmt.Println("The addition is: ", c)
	fmt.Println("The substrion is: ", d)
	fmt.Println("The multiplacation is: ", e)
	fmt.Println("The Division is: ", f)
}
